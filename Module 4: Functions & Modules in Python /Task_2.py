import math

# Ask the user for a number as input
num = float(input("Enter a number: "))

# Calculate the square root
sqrt_result = math.sqrt(num)

# Calculate the natural logarithm (log base e)
log_result = math.log(num)

# Calculate the sine of the number (in radians)
sine_result = math.sin(num)

# Display the results
print(f"\n")
print(f"Square root: {sqrt_result}")
print(f"Logarithm: {log_result}")
print(f"Sine: {sine_result}")
